package com.forum.lottery.utils.betUtil;

public class Layout {

	private String title;
	
	private String no;
	
	private int place;
	
	private int cols;
	
	private int minchosen;

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getNo() {
		return no;
	}

	public void setNo(String no) {
		this.no = no;
	}

	public int getPlace() {
		return place;
	}

	public void setPlace(int place) {
		this.place = place;
	}

	public int getCols() {
		return cols;
	}

	public void setCols(int cols) {
		this.cols = cols;
	}

	public int getMinchosen() {
		return minchosen;
	}

	public void setMinchosen(int minchosen) {
		this.minchosen = minchosen;
	}
		
	
}
